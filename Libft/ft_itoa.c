#include "libft.h"
#include <stdlib.h>

char	*ft_itoa(int n)
{
	char	*a;

	if (!(a = (char *)malloc(12)))
		return (NULL);
	if (n < 0)
	{
		if (n == -2147483648)
			return (a = "-2147483648");
		else
		{
			n *= -1;
			*a = '-';
		}
	}
	a += 11;
	*a-- = '\0';
	while (n != 0)
	{
		*a-- = '0' + n % 10;
		n /= 10;
	}
	return (a);
}
